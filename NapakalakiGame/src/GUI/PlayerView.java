    public void fillTreasurePanel(JPanel aPanel, ArrayList<Treasure> aList){
        //Se elimina informacion antigua
        aPanel.removeAll();
        for(Treasure t : aList){
            TreasureView aTreasureView = new TreasureView();
            aTreasureView.setTreasure(t);
            aTreasureView.setVisible(true);
            aPanel.add(aTreasureView);
        }
        //Se fuerza la actualizacion visual del panel
        aPanel.repaint();
        aPanel.revalidate();
        
    }
    
    public void setPlayer(Player p){
        playerModel = p;
        
        //Nombre
        tfNombre.setText(playerModel.getName());
        //Bonus minimo
        tfNivel.setText(Integer.toString(playerModel.getLevel()));
        //Tesoros visibles
        fillTreasurePanel(panelTV, playerModel.getVisibleTreasures());
        //Tesoros ocultos
        fillTreasurePanel(panelTH, playerModel.getHiddenTreasures());
        
        nTesorosOcultos.setText(Integer.toString(playerModel.getHiddenTreasures().size()));
        nTesorosVisibles.setText(Integer.toString(playerModel.getVisibleTreasures().size()));
        repaint();
        revalidate();
    }
    
    public void setNapakalaki(Napakalaki n) {
        napakalakiModel = n; 
        repaint(); 
    }
    
    public ArrayList<Treasure> getSelectedTreasures(JPanel aPanel) {
        // Se recorren los tesoros que contiene el panel,
        //    almacenando en un vector aquellos que est√°n seleccionados.
        //    Finalmente se devuelve dicho vector.
        TreasureView tv;
        ArrayList<Treasure> output = new ArrayList();
        for (Component c : aPanel.getComponents()) {
           tv = (TreasureView) c;
           if ( tv.isSelected() )
              output.add ( tv.getTreasure() );
        }
        return output;
     }
    
